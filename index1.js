document.addEventListener("DOMContentLoaded", function () {
    // Make the first list item bold
    var firstItem = document.getElementById("first-item");
    firstItem.style.fontWeight = "bold";
  
    // Make the second list item bold
    var secondItem = document.getElementById("second-item");
    secondItem.style.fontWeight = "bold";
  
    // Make the third list item bold and change its background color to yellow
    var thirdItem = document.getElementById("third-item");
    thirdItem.style.fontWeight = "bold";
    thirdItem.style.backgroundColor = "yellow";
  
    // Make the fourth list item bold
    var fourthItem = document.getElementById("fourth-item");
    fourthItem.style.fontWeight = "bold";
  });
  